package de.tum.in.far.threedui.control.general;

public class AngleTuple {
	
	private double a;
	private double b;
	public AngleTuple(double a, double b) {
		super();
		this.a = a;
		this.b = b;
	}
	public double getA() {
		return a;
	}
	public void setA(double a) {
		this.a = a;
	}
	public double getB() {
		return b;
	}
	public void setB(double b) {
		this.b = b;
	}
}
